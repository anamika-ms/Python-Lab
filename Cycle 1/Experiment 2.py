# Aim: 
# Write a program to demonstrate different number data types in python.

# Pseudocode:
# 1. Assign int_num=2, float_num=2.44,complex_num=2+4j.
# 2. Print type of int_num, float_num, complex_num.

int_num=10
float_num=10.5
complex_num=2+3j
print(f"Integer : {int_num} , Type : {type(int_num)}")
print(f"Float : {float_num} , Type : {type(float_num)}")
print(f"Complex : {complex_num} , Type : {type(complex_num)}")

# Output:
# Integer:2 Type:<class ‘int’>
# Float:2.44 Type:<class ‘float’>
# Complex:(2+4j) Type:<class ‘complex’>
